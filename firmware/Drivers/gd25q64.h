#ifndef YUEXINMIAO_GD25Q64_H
#define YUEXINMIAO_GD25Q64_H

#include <stdint.h>

/* ======================== 器件固定参数 ======================== */

#define GD25Q64_CAPACITY_BYTES       0x00800000UL  /* 64Mbit，即 8MiB，三字节地址范围 0x000000～0x7FFFFF。 */
#define GD25Q64_EXPECTED_JEDEC_ID    0x00C84017UL  /* 制造商 C8、存储类型 40、容量代码 17。 */

typedef enum
{
    GD25Q64_STATUS_OK = 0,                /* 操作成功。 */
    GD25Q64_STATUS_INVALID_PARAM = 1,     /* 空指针、地址越界、长度越界或流状态冲突。 */
    GD25Q64_STATUS_SPI_TIMEOUT = 2,       /* SPI0 忙状态在软件超时边界内未结束。 */
    GD25Q64_STATUS_ID_MISMATCH = 3,       /* 读到的 JEDEC ID 不是 C8 40 17。 */
    GD25Q64_STATUS_READ_NOT_OPEN = 4      /* 未执行 BeginRead 就调用 ReadByte。 */
} Gd25q64Status;

/**
 ******************************************************************************
  @功能：初始化 SPI0 并核对 GD25Q64 JEDEC ID
  @日期：2026-07-13
  @参数：无
  @返回值：Gd25q64Status - ID 为 C8 40 17 时返回 GD25Q64_STATUS_OK
  @使用说明：调用前必须使能 SPI0 模块时钟；使用 PC0=CS、PC1=SCLK、PC2=MISO、PC3=MOSI。
             接口配置为 Mode0、8 位、10MHz，当前模块只实现只读播放，不提供擦除和编程接口。
 ******************************************************************************
 */
Gd25q64Status GD25Q64_Init(void);

/**
 ******************************************************************************
  @功能：读取 GD25Q64 的三字节 JEDEC 标识
  @日期：2026-07-13
  @参数：[输出] jedec_id - 返回值低 24 位依次保存制造商、存储类型和容量代码
  @返回值：Gd25q64Status - 参数非法或 SPI 超时时返回对应错误
  @使用说明：调用前必须完成 SPI0 时钟和引脚配置；函数内部独立控制一次 CS# 事务。
 ******************************************************************************
 */
Gd25q64Status GD25Q64_ReadJedecId(uint32_t *jedec_id);

/**
 ******************************************************************************
  @功能：从指定 Flash 地址连续读取一段数据
  @日期：2026-07-13
  @参数：[输入] address - GD25Q64 三字节绝对地址
        [输出] buffer - 接收数据的调用者缓冲区
        [输入] length - 读取长度，单位字节
  @返回值：Gd25q64Status - 地址、指针、长度或 SPI 操作异常时返回对应错误
  @使用说明：函数会自动完成 BeginRead/ReadByte/EndRead；buffer 在函数返回前由调用者独占。
 ******************************************************************************
 */
Gd25q64Status GD25Q64_Read(uint32_t address, uint8_t *buffer, uint32_t length);

/**
 ******************************************************************************
  @功能：发送普通读命令和三字节地址，打开连续读取事务
  @日期：2026-07-13
  @参数：[输入] address - 首字节绝对地址，范围 0x000000～0x7FFFFF
  @返回值：Gd25q64Status - 成功后 CS# 保持低电平，状态变为已打开
  @使用说明：成功后只能调用 GD25Q64_ReadByte，结束时必须调用 GD25Q64_EndRead；不可嵌套打开。
 ******************************************************************************
 */
Gd25q64Status GD25Q64_BeginRead(uint32_t address);

/**
 ******************************************************************************
  @功能：在已打开的连续读取事务中接收一个字节
  @日期：2026-07-13
  @参数：[输出] data - 接收一个 Flash 数据字节
  @返回值：Gd25q64Status - 流未打开、参数非法或 SPI 超时时返回对应错误
  @使用说明：SPI 超时时函数会自动结束读取事务，调用者仍可安全再次调用 EndRead。
 ******************************************************************************
 */
Gd25q64Status GD25Q64_ReadByte(uint8_t *data);

/**
 ******************************************************************************
  @功能：拉高 CS# 并关闭当前连续读取事务
  @日期：2026-07-13
  @参数：无
  @返回值：无
  @使用说明：允许在未打开状态重复调用，用于统一清理所有错误退出路径。
 ******************************************************************************
 */
void GD25Q64_EndRead(void);

#endif
