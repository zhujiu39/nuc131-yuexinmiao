#ifndef YUEXINMIAO_LCD_DRIVER_H
#define YUEXINMIAO_LCD_DRIVER_H

#include <stdint.h>

/* ======================== RGB565 常用颜色 ======================== */

#define LCD_COLOR_BLACK          0x0000U
#define LCD_COLOR_BLUE           0x001FU
#define LCD_COLOR_GREEN          0x07E0U
#define LCD_COLOR_CYAN           0x07FFU
#define LCD_COLOR_RED            0xF800U
#define LCD_COLOR_MAGENTA        0xF81FU
#define LCD_COLOR_YELLOW         0xFFE0U
#define LCD_COLOR_WHITE          0xFFFFU

typedef enum
{
    LCD_STATUS_OK = 0,                 /* 接口参数与流状态有效，写操作已同步完成。 */
    LCD_STATUS_INVALID_PARAM = 1      /* 坐标、尺寸、计数或连续写入状态不合法。 */
} LcdStatus;

/**
 ******************************************************************************
  @功能：初始化参考工程同款 128x128 RGB565 LCD
  @日期：2026-07-13
  @参数：无
  @返回值：LcdStatus - 当前写通道无法读回控制器，完成初始化后返回 LCD_STATUS_OK
  @使用说明：调用前必须完成系统时钟初始化；函数内部配置 GPIO、执行参考工程初始化序列、
             清黑屏并打开背光。LCD 无读回线，OK 只表示软件写流程完成。
 ******************************************************************************
 */
LcdStatus LCD_Init(void);

/**
 ******************************************************************************
  @功能：控制 LCD 背光
  @日期：2026-07-13
  @参数：[输入] enabled - 0 关闭背光，非 0 打开背光
  @返回值：无
  @使用说明：PC6 为高有效背光控制脚，调用前应先执行 LCD_Init。
 ******************************************************************************
 */
void LCD_SetBacklight(uint8_t enabled);

/**
 ******************************************************************************
  @功能：使用单一 RGB565 颜色清屏
  @日期：2026-07-13
  @参数：[输入] color - RGB565 颜色值
  @返回值：LcdStatus - 参数有效时返回 LCD_STATUS_OK
  @使用说明：函数会同步刷新全部 128x128 像素，仅用于启动清屏和错误纯色提示，不用于 30Hz 动画帧。
 ******************************************************************************
 */
LcdStatus LCD_Clear(uint16_t color);

/**
 ******************************************************************************
  @功能：打开一个连续像素写入窗口
  @日期：2026-07-13
  @参数：[输入] x - 左上角 X 坐标
        [输入] y - 左上角 Y 坐标
        [输入] width - 区域宽度
        [输入] height - 区域高度
  @返回值：LcdStatus - 参数合法时返回 LCD_STATUS_OK
  @使用说明：成功后 LCD 片选保持有效，必须与 LCD_EndRegionWrite 成对调用。坐标使用逻辑屏幕范围，
             驱动内部自动叠加 X+1、Y+2 控制器偏移；接口不可嵌套，也不可在中断中调用。
 ******************************************************************************
 */
LcdStatus LCD_BeginRegionWrite(uint16_t x, uint16_t y, uint16_t width, uint16_t height);

/**
 ******************************************************************************
  @功能：向已打开的 LCD 窗口写入一个 RGB565 像素
  @日期：2026-07-13
  @参数：[输入] color - RGB565 颜色
  @返回值：LcdStatus - 未打开连续写入窗口或参数非法时返回 LCD_STATUS_INVALID_PARAM
  @使用说明：仅在 LCD_BeginRegionWrite 与 LCD_EndRegionWrite 之间调用；按高字节、低字节顺序写屏。
 ******************************************************************************
 */
LcdStatus LCD_StreamWritePixel(uint16_t color);

/**
 ******************************************************************************
  @功能：向已打开的 LCD 窗口连续写入相同 RGB565 像素
  @日期：2026-07-13
  @参数：[输入] color - RGB565 颜色
        [输入] count - 重复写入次数，范围 1～65535
  @返回值：LcdStatus - 流未打开或 count 为 0 时返回 LCD_STATUS_INVALID_PARAM
  @使用说明：供 PackBits 重复色块和矩形填充使用，整个过程保持 LCD CS 为低电平。
 ******************************************************************************
 */
LcdStatus LCD_StreamWriteRepeat(uint16_t color, uint16_t count);

/**
 ******************************************************************************
  @功能：结束连续像素写入窗口
  @日期：2026-07-13
  @参数：无
  @返回值：无
  @使用说明：拉高 LCD CS 并清除流状态；可在错误清理路径重复调用。
 ******************************************************************************
 */
void LCD_EndRegionWrite(void);

/**
 ******************************************************************************
  @功能：填充指定矩形区域
  @日期：2026-07-13
  @参数：[输入] x - 左上角 X 坐标
        [输入] y - 左上角 Y 坐标
        [输入] width - 区域宽度
        [输入] height - 区域高度
        [输入] color - RGB565 颜色值
  @返回值：LcdStatus - 坐标越界或宽高为 0 时返回 LCD_STATUS_INVALID_PARAM
  @使用说明：坐标范围为 X=0..127、Y=0..127，区域不能超出屏幕边界。函数同步完成全部像素写入。
 ******************************************************************************
 */
LcdStatus LCD_Fill(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint16_t color);

/**
 ******************************************************************************
  @功能：绘制一个像素点
  @日期：2026-07-13
  @参数：[输入] x - X 坐标
        [输入] y - Y 坐标
        [输入] color - RGB565 颜色值
  @返回值：LcdStatus - 坐标越界时返回 LCD_STATUS_INVALID_PARAM
  @使用说明：每次调用都会重新设置 1x1 窗口；高频连续绘制应使用流式接口，避免重复开窗。
 ******************************************************************************
 */
LcdStatus LCD_DrawPixel(uint16_t x, uint16_t y, uint16_t color);

/**
 ******************************************************************************
  @功能：使用 Bresenham 算法绘制线段
  @日期：2026-07-13
  @参数：[输入] x0 - 起点 X 坐标
        [输入] y0 - 起点 Y 坐标
        [输入] x1 - 终点 X 坐标
        [输入] y1 - 终点 Y 坐标
        [输入] color - RGB565 颜色值
  @返回值：LcdStatus - 任一端点越界时返回 LCD_STATUS_INVALID_PARAM
  @使用说明：逐点写屏，适合低频基础图元，不用于高频大面积刷新。
 ******************************************************************************
 */
LcdStatus LCD_DrawLine(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1, uint16_t color);

#endif
