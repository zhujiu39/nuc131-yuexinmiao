/**
 ******************************************************************************
  @功能：处理 Cortex-M0 HardFault 并执行受控复位
  @日期：2026-07-13
  @参数：[输入] lr - 异常返回地址
        [输入] msp - 主堆栈指针
        [输入] psp - 进程堆栈指针
  @返回值：uint32_t - 正常情况下系统复位且不返回；保留返回值满足厂商启动文件接口
  @使用说明：当前最小工程尚无持久化日志和看门狗，严重异常时先关闭背光再请求系统复位。
 ******************************************************************************
 */
uint32_t ProcessHardFault(uint32_t lr, uint32_t msp, uint32_t psp)
{
    (void)msp;
    (void)psp;

    PC6 = 0;
    NVIC_SystemReset();
    return lr;
}

